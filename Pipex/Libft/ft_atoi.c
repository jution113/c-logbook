#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int			sign;
	long long	res;

	sign = 1;
	res = 0;
	while (*nptr == 32 || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '+' || *nptr == '-')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	while (ft_isdigit(*nptr))
	{
		res *= 10;
		res += *nptr - '0';
		nptr++;
	}
	return ((int)(res * sign));
}
